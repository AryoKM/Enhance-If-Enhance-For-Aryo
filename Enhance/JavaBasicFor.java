
public class JavaBasicFor {
    public static void main(String[] args){
        for(int i = 0; i < 10; i++){ // mendefinisikan loop for
         System.out.println("Perulangan ke " + (i+1)); // untuk setiap perulangan for, kode di dalam perulangan dijalankan. Variabel i bertambah 1 menggunakan operator ++ tiap terjalankan.
        }
    }
}

// proram ini menunjukkan sintaks dasar perulangan for di Java dan untuk menunjukkan bagaimana perulangan dapat digunakan untuk mengulang blok kode beberapa kali yang telah tertentu.
// program ini mencetak pesan ke konsol sepuluh kali, menggunakan for loop.
