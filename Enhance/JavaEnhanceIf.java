
public class JavaEnhanceIf{
    public static void main(String[] args){
        int i = 5; // mendeklarasikan variabel integer i dan menginisialisasinya dengan nilai 5
        System.out.println((i > 5) ? "i lebih besar dari 5" : (i < 5) ? "i lebih kecil dari 5" : "i sama dengan 5");// menggunakan operator ternary untuk mencetak nilai berdasarkan nilai i
    }
}

// program ini mengevaluasi ekspresi i jika lebih besar dari pada 5, lebih kecil dari pada 5, atau sama dengan 5 dan mengembalikan pesan string yang sesuai berdasarkan hasilnya